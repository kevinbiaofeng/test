import com.xjw.base.entity.BasePo;

public class WithdrawalFinance extends BasePo{
	private static final long serialVersionUID = 1L;
	private String loginName;//用户名
	private Long financeUser;
	private int countFinance;
	public String getLoginName() {
		return loginName;
	}
	public void setLoginName(String loginName) {
		this.loginName = loginName;
	}
	public Long getFinanceUser() {
		return financeUser;
	}
	public void setFinanceUser(Long financeUser) {
		this.financeUser = financeUser;
	}
	public int getCountFinance() {
		return countFinance;
	}
	public void setCountFinance(int countFinance) {
		this.countFinance = countFinance;
	}
	
}
