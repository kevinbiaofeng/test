package com.xjw.entity.po.sys;

import java.math.BigDecimal;
import java.util.Date;

import com.xjw.base.entity.BasePo;

/**
 * 昨日派奖总额
 */
public class YestdayAmountLog extends BasePo {
	private static final long serialVersionUID = -8717924052254007776L;
	
	/** 当日总额 */
	private BigDecimal dayAmount;
	/** 最小金额 */
	private Integer minAmount;
	/** 最大金额 */
	private Integer maxAmount;
	/** 统计时间 */
	private Date statTime;

	public BigDecimal getDayAmount() {
		return dayAmount;
	}

	public void setDayAmount(BigDecimal dayAmount) {
		this.dayAmount = dayAmount;
	}

	public Integer getMinAmount() {
		return minAmount;
	}

	public void setMinAmount(Integer minAmount) {
		this.minAmount = minAmount;
	}

	public Integer getMaxAmount() {
		return maxAmount;
	}

	public void setMaxAmount(Integer maxAmount) {
		this.maxAmount = maxAmount;
	}

	public Date getStatTime() {
		return statTime;
	}

	public void setStatTime(Date statTime) {
		this.statTime = statTime;
	}
}
