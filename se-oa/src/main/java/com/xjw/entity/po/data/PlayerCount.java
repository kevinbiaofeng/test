package com.xjw.entity.po.data;

import java.math.BigDecimal;

import com.xjw.base.entity.BasePo;

public class PlayerCount extends BasePo{
	private static final long serialVersionUID = 1L;
	/** 会员ID */
	private Long userId;
	/** 会员账号 */
	private String loginName;
	/** 存款金额 */
	private BigDecimal depositAmount = BigDecimal.ZERO;
	/** 取款金额 */
	private BigDecimal withdrawAmount = BigDecimal.ZERO;
	/** 红利金额 */
	private BigDecimal memberMoney = BigDecimal.ZERO;
	
	public Long getUserId() {
		return userId;
	}
	public void setUserId(Long userId) {
		this.userId = userId;
	}
	public String getLoginName() {
		return loginName;
	}
	public void setLoginName(String loginName) {
		this.loginName = loginName;
	}
	public BigDecimal getDepositAmount() {
		return depositAmount;
	}
	public void setDepositAmount(BigDecimal depositAmount) {
		this.depositAmount = depositAmount;
	}
	public BigDecimal getWithdrawAmount() {
		return withdrawAmount;
	}
	public void setWithdrawAmount(BigDecimal withdrawAmount) {
		this.withdrawAmount = withdrawAmount;
	}
	public BigDecimal getMemberMoney() {
		return memberMoney;
	}
	public void setMemberMoney(BigDecimal memberMoney) {
		this.memberMoney = memberMoney;
	}
	
}
