package com.xjw.entity.po.activity;

import java.math.BigDecimal;
import java.util.Date;

import com.xjw.base.entity.BasePo;

public class Rebate extends BasePo {
	private static final long serialVersionUID = 1L;
	private String name;
	private Integer memberStart;
	private Integer memberEnd;
	private Integer platformType;
	private BigDecimal percent;
	private BigDecimal up;
	private BigDecimal lower;
	private Date startTime;
	private Date endTime;
	private String remark;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getMemberStart() {
		return memberStart;
	}

	public void setMemberStart(Integer memberStart) {
		this.memberStart = memberStart;
	}

	public Integer getMemberEnd() {
		return memberEnd;
	}

	public void setMemberEnd(Integer memberEnd) {
		this.memberEnd = memberEnd;
	}

	public Integer getPlatformType() {
		return platformType;
	}

	public void setPlatformType(Integer platformType) {
		this.platformType = platformType;
	}

	public BigDecimal getPercent() {
		return percent;
	}

	public void setPercent(BigDecimal percent) {
		this.percent = percent;
	}

	public BigDecimal getUp() {
		return up;
	}

	public void setUp(BigDecimal up) {
		this.up = up;
	}

	public BigDecimal getLower() {
		return lower;
	}

	public void setLower(BigDecimal lower) {
		this.lower = lower;
	}

	public Date getStartTime() {
		return startTime;
	}

	public void setStartTime(Date startTime) {
		this.startTime = startTime;
	}

	public Date getEndTime() {
		return endTime;
	}

	public void setEndTime(Date endTime) {
		this.endTime = endTime;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
}
