package com.xjw.entity.form.media;

import com.xjw.entity.form.BaseForm;
import com.xjw.kzenum.sys.YesORNoEnum;

public class VideoForm extends BaseForm {
	private static final long serialVersionUID = 1752240807127110596L;

	/** 素材编号 */
	private Long id;
	/** 素材CODE */
	private String code;
	/** 标题 */
	private String title;
	/** 主演 */
	private String star;
	/** 类型 {link VideoTypeEnum} */
	private Integer type;
	/** 会员视频 {link YesORNoEnum} */
	private Integer vipFlag;
	/** 显示状态 {@link YesORNoEnum} */
	private Integer status;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getStar() {
		return star;
	}

	public void setStar(String star) {
		this.star = star;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public Integer getVipFlag() {
		return vipFlag;
	}

	public void setVipFlag(Integer vipFlag) {
		this.vipFlag = vipFlag;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

}
