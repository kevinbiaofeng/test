package com.xjw.entity.po.message;

import java.util.Date;

import com.xjw.base.entity.BasePo;
import com.xjw.kzenum.sys.YesORNoEnum;

/**
 * 站内信
 */
public class Message extends BasePo {
	private static final long serialVersionUID = 8664378549141491131L;

	/** 标题 */
	private String title;
	/** 内容 */
	private String content;
	/** 收件人总数 */
	private Integer receiveCount;
	
	
	/** 收件人ID */
	private Long receiveUser;
	/** 收件人账号 */
	private String receiveName;
	/** 阅读状态 {@link YesORNoEnum} */
	private Integer readStatus;
	/** 阅读时间 */
	private Date readTime;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public Integer getReceiveCount() {
		return receiveCount;
	}

	public void setReceiveCount(Integer receiveCount) {
		this.receiveCount = receiveCount;
	}

	public Long getReceiveUser() {
		return receiveUser;
	}

	public void setReceiveUser(Long receiveUser) {
		this.receiveUser = receiveUser;
	}

	public String getReceiveName() {
		return receiveName;
	}

	public void setReceiveName(String receiveName) {
		this.receiveName = receiveName;
	}

	public Integer getReadStatus() {
		return readStatus;
	}

	public void setReadStatus(Integer readStatus) {
		this.readStatus = readStatus;
	}

	public Date getReadTime() {
		return readTime;
	}

	public void setReadTime(Date readTime) {
		this.readTime = readTime;
	}
}
