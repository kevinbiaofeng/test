package com.xjw.entity.po.sys;

import com.xjw.base.entity.BasePo;
import com.xjw.kzenum.order.TradeModeEnum;

/**
 * 支付配置
 */
public class PayConfig extends BasePo {
	private static final long serialVersionUID = -8854148832724902501L;

	/** 支付方式 {@link TradeModeEnum} */
	private Integer tradeMode;
	/** 状态 */
	private Integer status;

	public Integer getTradeMode() {
		return tradeMode;
	}

	public void setTradeMode(Integer tradeMode) {
		this.tradeMode = tradeMode;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}
}
